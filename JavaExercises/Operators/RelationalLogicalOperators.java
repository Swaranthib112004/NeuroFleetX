package Operators;

public class RelationalLogicalOperators {
    public static void main(String[] args) {
        int x = 20, y = 15;
        boolean a = true, b = false;

        System.out.println("x > y: " + (x > y));
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("a && b: " + (a && b));
        System.out.println("a || b: " + (a || b));
        System.out.println("!a: " + (!a));
    }
}
